#Graph-3
import numpy as np
import matplotlib.pyplot as plt

x = np.linspace(0,100,200)
y = np.linspace(0,100,200)
X,Y = np.meshgrid(x,y)

def gaussian(x0,y0,amp,sigma):
    return amp*np.exp(-((X-x0)**2+(Y-y0)**2)/(2*sigma**2))

R = gaussian(30,35,6,12) + gaussian(70,75,5,10)

u = 0.6
v = 0.4

U = np.ones_like(X)*u
V = np.ones_like(Y)*v
dt = 10
shift_x = int(u*dt)
shift_y = int(v*dt)

R_forecast = np.roll(R, shift_x, axis=1)
R_forecast = np.roll(R_forecast, shift_y, axis=0)
fig, ax = plt.subplots(1,2,figsize=(12,5))

c1 = ax[0].contourf(X,Y,R,25,cmap="YlOrRd")

step = 25
mask = R[::step,::step] > 0.5

ax[0].quiver(
    X[::step,::step][mask],
    Y[::step,::step][mask],
    U[::step,::step][mask],
    V[::step,::step][mask],
    color="navy",
    scale=10
)

ax[0].set_title("Observed $R(x,t)$ + Velocity $V(x,t)$")
ax[0].set_xlabel("x (km)")
ax[0].set_ylabel("y (km)")

plt.colorbar(c1,ax=ax[0],label="Rain rate (mm/h)")

c2 = ax[1].contourf(X,Y,R_forecast,25,cmap="YlOrRd")

ax[1].set_title("Forecast $\hat{R}(x,t+Δt)$")
ax[1].set_xlabel("x (km)")
ax[1].set_ylabel("y (km)")

plt.colorbar(c2,ax=ax[1],label="Rain rate (mm/h)")

plt.tight_layout()
plt.show()


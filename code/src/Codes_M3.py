#Graph-1
import numpy as np
import matplotlib.pyplot as plt
from scipy.stats import lognorm, norm

mu = 0.0
sigma = 0.8

R = np.linspace(0.001, 10, 500)

pdf_R = lognorm.pdf(R, s=sigma, scale=np.exp(mu))

X = 10 * np.log10(R)

mu_X = np.mean(X)
sigma_X = np.std(X)

x_vals = np.linspace(-30, 25, 500)
pdf_X = norm.pdf(x_vals, mu_X, sigma_X)

fig, ax = plt.subplots(1, 2, figsize=(12,5))

ax[0].plot(R, pdf_R, color="blue", linewidth=2)
ax[0].fill_between(R, pdf_R, alpha=0.3, color="blue")
ax[0].axvline(0.1, linestyle="--", color="red", label="0.1 mm/h threshold")

ax[0].set_title("Raw State Variable\nR(x,t) ~ LogNormal(μ,σ²)")
ax[0].set_xlabel("Rainfall Intensity R (mm/h)")
ax[0].set_ylabel("Probability Density")
ax[0].legend()

ax[1].plot(x_vals, pdf_X, color="orange", linewidth=2)
ax[1].fill_between(x_vals, pdf_X, alpha=0.3, color="orange")
ax[1].axvline(-15, linestyle="--", color="red", label="-15 dBR dry pixel floor")

ax[1].set_title("Transformed Latent State\nX = 10 log10(R)")
ax[1].set_xlabel("Log-Transformed Intensity X (dBR)")
ax[1].set_ylabel("Probability Density")
ax[1].legend()

plt.tight_layout()
plt.show()

